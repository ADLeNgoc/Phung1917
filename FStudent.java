    public void showdulieu() {
        try {
            fTable.removeAll();
            String[] arr = {"Student_ID", "NAME", "AGE"};
            DefaultTableModel model = new DefaultTableModel(arr, 0);
            String query = "select *FROM dbo.[QLSVOFF]";
            Connection con = DriverManager.getConnection(dbURL,dbUser,dbPassword);
            Statement stmt = con.createStatement();
            preparedStatement ps = (preparedStatement) con.prepareStatement(query);
            ResultSet rs = ps.executeQuery();
            while (rs.next()){
            Vector vector= new Vector();
            vector.add(rs.getInt("Student_ID"));
            vector.add(rs.getString("NAME"));
            vector.add(rs.getInt("AGE"));
            model.addRow(vector);
        }
            fTable.setModel(model);
        } catch (SQLException ex) {
            Logger.getLogger(FStudent.class.getName()).log(Level.SEVERE, null, ex);
        }
            
    }

