    private void fSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_fSaveActionPerformed
       if(fStudent_ID.getText().equals("")){
           JOptionPane.showMessageDialog(null, "ID sinh viên không được rỗng");
           fStudent_ID.requestFocus();
           return;
       }else if(fName.getText().equals("")){
           JOptionPane.showMessageDialog(null, "Tên sinh viên không được rỗng"); 
           fName.requestFocus();
           return;
       }else if(fAge.getText().equals("")){
           JOptionPane.showMessageDialog(null, "Tuổi sinh viên không được rỗng");
           fAge.requestFocus();
           return;
       }
       String id = fStudent_ID.getText();
       if(fStudent_ID.getText().length() !=4){
           JOptionPane.showMessageDialog(null, "ID sinh viên phải có 4 ký tự");
           fStudent_ID.requestFocus();
           return; 
       }else{
           boolean id1 = Character.isDigit(id.charAt(0));
           boolean id2 = Character.isDigit(id.charAt(1));
           boolean id3 = Character.isDigit(id.charAt(2));
           boolean id4 = Character.isDigit(id.charAt(3));
           if(id1 && id2 && id3 && id4 ){
            JOptionPane.showMessageDialog(null, "chuẩn");
           return;  }
       else {
           JOptionPane.showMessageDialog(null, "Mã Sinh viên phải là số!");
           return;
               }
       
    }//GEN-LAST:event_fSaveActionPerformed
    }
